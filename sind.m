% Function: Sine in degrees             %
% Input: Some angle x                   %
% Output: Sine of the angle in degrees  %
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
function s = sind(x)
  %SIND(X) Calculate sine(x) in degrees
  s = sin(x*pi/180);
endfunction
